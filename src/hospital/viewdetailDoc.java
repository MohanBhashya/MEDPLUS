    private void fetchDataFromDatabase() {
        synchronized (lock) { // Acquire the lock for mutual exclusion
            try {
                Class.forName("com.mysql.jdbc.Driver");
                Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/hms", "root", "Sl@372005");
                Statement st = conn.createStatement();
                String sql = "select * from doctor_record";
                PreparedStatement pstmt = conn.prepareStatement(sql);
                ResultSet rs = pstmt.executeQuery();
                DefaultTableModel tm = (DefaultTableModel) jTable1.getModel();
                tm.setRowCount(0);
                while (rs.next()) {
                    Object o[] = {rs.getInt("ID"), rs.getString("DOCTORNAME"), rs.getString("SPECIALIZATION")};
                    tm.addRow(o);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }

